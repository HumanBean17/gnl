#include "libft.h"

char		*ft_strstr(const char *str, const char *to_find)
{
	int i;
	int j;
	int len;

	len = 0;
	i = 0;
	len = ft_strlen(to_find);
	if (to_find[0] == '\0')
		return ((char *)str);
	while (str[i] != '\0')
	{
		j = 0;
		while (str[j + i] == to_find[j])
		{
			if (j + 1 == len)
				return ((char *)(str + i));
			j++;
		}
		i++;
	}
	return (NULL);
}
